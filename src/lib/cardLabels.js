export function titleFor(img) {
  const arr = TITLES[img.subgroup] || ['—'];
  return arr[(img.id - 1) % arr.length];
}

export function descriptionFor(img) {
  const arr = DESCRIPTIONS[img.subgroup] || [''];
  return arr[(img.id - 1) % arr.length];
}

export function timestampFor(img) {
  const t = (img.id * 137) % 5400; // up to 90:00
  const m = Math.floor(t / 60);
  const s = t % 60;
  return `${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}`;
}

